public class Main {
    public static void main(String[] args) {
        // expression = operands & operators
        // operands = values, variables, numbers, quantity
        // operators = 1 - * / %

        int friends = 10;

        friends = friends + 1;
        System.out.println(friends);

        friends = friends * 2;
        System.out.println(friends);

        friends = friends / 2;
        System.out.println(friends);

        friends = friends % 3;
        System.out.println(friends);

        friends++;
        System.out.println(friends);

        friends--;
        System.out.println(friends);
    }
}